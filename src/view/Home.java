    private void btnTrapzoidalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTrapzoidalMouseClicked
        if(txtA.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe indicar un valor para el límite interior");
            txtA.requestFocus();;
            txtA.selectAll();
        } else if(txtB.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe indicar un valor para el límite superior");
            txtB.requestFocus();;
            txtB.selectAll();
        } else if(txtN.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe indicar un valor par (n)");
            txtN.requestFocus();;
            txtN.selectAll();
        } else if(txtFx.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe seleccionar y cargar una función (vea la barra superior");
        } else {
        
            float delta = (Float.parseFloat(txtB.getText()) - Float.parseFloat(txtA.getText())) / Float.parseFloat(txtN.getText());
            lblMetodo.setText("Trapezoidal");
            lblDelta.setText("Δ = " + String.format("%,.2f", delta));
            String signo;

            setModelo(txtFx.getText());

            int n = Integer.parseInt(txtN.getText()) + 1;
            float x = Float.parseFloat(txtA.getText());
            double res = 0;
            float k = 0;
            double sumatoria = 0;
            String fx = "";
            String coef = "";

            System.out.println("Operacion: " + this.oper);

            for(int i=1; i<=n; i++){
                coef = "";

                switch(this.oper){
                    case 1:
                        signo = (btnSignoRaiz.isSelected())? "-" : "+";
                        k = Float.parseFloat(txtVarRaiz.getText());
                        res = (signo.equals("+"))? Math.sqrt(x + k) : Math.sqrt(x - k);
                        fx = "√(" + String.format("%,.2f", x) + btnSignoRaiz.getText() + txtVarRaiz.getText() + ")";
                        break;

                    case 2:
                        signo = (btnSignoLog.isSelected())? "-" : "+";
                        k = Float.parseFloat(txtVarLog.getText());
                        res = (signo.equals("+"))? Math.log10(x + k) : Math.log10(x - k);
                        fx = "Log(" + String.format("%,.2f", x) + btnSignoLog.getText() + txtVarLog.getText() + ")";
                        break;

                    case 3:
                        signo = (btnSignoLn.isSelected())? "-" : "+";
                        k = Float.parseFloat(txtVarLn.getText());
                        res = (signo.equals("+"))? Math.log(x + k) : Math.log(x - k);
                        fx = "Ln(" + String.format("%,.2f", x) + btnSignoLn.getText() + txtVarLn.getText() + ")";
                        break;

                    case 4:
                        k = Float.parseFloat(txtVarExp.getText());
                        res = Math.pow(k, x);
                        fx = txtVarExp.getText() + "^" + String.format("%,.2f", x);
                        break;
                }
                
                if(Double.isNaN(res) || Double.isInfinite(res)) res = 0;

                if(i != 1 && i != n) {
                    res *= 2;
                    coef = "(2) ";
                }

                fx = coef + fx;

                setParticion(i, String.format("%,.2f", x), fx, String.format("%,.8f", res));

                sumatoria += res;
                System.out.println("X: " + x + " Res: " + res);
                x += delta;
            }

            System.out.println("Sumatoria: " + sumatoria);
            lblArea.setText(String.format("%,.8f", (delta / 2) * sumatoria));
            txtSumatoria.setText(String.format("%,.8f", sumatoria));
        }
    }//GEN-LAST:event_btnTrapzoidalMouseClicked

