    private void setModelo(String fx){
        String[] titulo = {"#", "X", "F(x)=" + fx, "Resultado"};
        dtmParticiones.setRowCount(0);
        dtmParticiones.setColumnIdentifiers(titulo);
        tblParticiones.setModel(dtmParticiones);
        initTabla();
    }
    
    private void setParticion(int i, String x, String fx, String res){
        Object[] particion = new Object[dtmParticiones.getColumnCount()];
        
        particion[0] = i;
        particion[1] = x;
        particion[2] = fx;
        particion[3] = res;
        
        dtmParticiones.addRow(particion);
    }

